/**
 * 打印出99乘法表的JavaScript代码
 * 使用双重循环实现乘法表的打印
 * 外层循环控制行数，内层循环控制每行的表达式数量
 */
//打印出99乘法表
for (let i = 1; i <= 9; i++) {  // 外层循环，控制乘法表的行数，从1到9
    for (let j = 1; j <= i; j++) {  // 内层循环，控制每行表达式的数量，当前行数为i时，表达式数量也为i
        console.log(`${j} * ${i} = ${i * j}`);  // 使用模板字符串输出乘法表达式，格式为"j * i = 结果"
    }
    console.log('---------------------');  // 每打印完一行乘法表后，输出分隔线
}